#include "main.h"

int main(void)
{
    char matrix[MAX_SIZE][MAX_SIZE] = {
        {'0','4','3','2','1','0'},
        {'2','0','0','0','0','3'},
        {'3','0','0','0','0','1'},
        {'3','0','0','0','0','2'},
        {'1','0','0','0','0','3'},
        {'0','1','2','2','2','0'}
        };
    char *top_indexes = (char *)malloc(8);
    int z = 0;
    for(int i = 0; i < MAX_SIZE ; i += MAX_SIZE-1)
    {
        for(int j = 1; j < MAX_SIZE - 1; j++)
        {
            top_indexes[z] = matrix[i][j];
            z++;
        }
    }

    solve(matrix , top_indexes);
    return 0;
}
